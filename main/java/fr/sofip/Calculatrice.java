package fr.sofip;

public class Calculatrice {
    public int somme(int a, int b){
        return a + b;
    }
    public int multi(int a, int b){
        return a * b;
    }
    public int soustract(int a, int b){
        return a - b;
    }
    public int divi(int a, int b){
        return a / b;
    }
}
